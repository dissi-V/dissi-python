# Arithmetic Operators - Perform simple calculations
x = 60
y = 7

print(x + y)  # Addition
print(x - y)  # Subtraction
print(x * y)  # Multiplication
print(x / y)  # Division
print(x % y)  # Modulus - Returns the remainder

# Comparison Operators - Used to compare values
a = 34
b = 23
print(a < b)
print(a > b)
print(a <= b)
print(a >= b)
print(a == b)  # Equal to
print(a != b)  # Not equal to

# Assignment operators
num = 56
print(num)

z = 2
z += 4
print(z)

number = 45
number *= 2
print(number)

# Logical operators
num1 = 23
num2 = 16
print(num1 > num2 and num1 == num2)
print(num1 > num2 or num1 == num2)
print(not(num1 > num2 and num1 == num2))
